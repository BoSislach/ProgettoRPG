public class Mana {
    private int mana;
    private String nome;

    public Mana(int mana, String nome) {
        this.mana = mana;
        this.nome = nome;
    }

    public String getNome() {
        return nome;
    }

    public int getMana() {
        return mana;
    }

    public void setMana(int mana) {
        this.mana = mana;
    }


    
}
